import "server-only";

/**
 * Step2（解析）のシステムプロンプト（PRD §11-S2）。
 *
 * 各文書（本願明細書・OA文書・引用文献・現クレーム等）を1件ずつ Claude に渡し、
 * 「内容概要（summary）」と「全文テキスト＝文字起こし（full_text）」を出力させる。
 * 出力の JSON 形（summary / full_text）は route 側の output_config（構造化出力）で固定するため、
 * ここでは役割・読み取り方針・品質基準のみを定義する。
 *
 * 文言はチューニング前提でこの定数に集約する（PRD §11「ハードコードせず定数/設定として分離」）。
 * 非PDF（.docx/.txt 等）は抽出済みテキストを渡して summary のみ求めるため、full_text の指示は
 * 該当文書のときだけ効く（求めない呼び出しでは output_config 側で full_text を要求しない）。
 */
export const S2_SYSTEM_PROMPT = `あなたは各国特許庁のオフィスアクション（OA）対応を支援する専門家です。
提示された1つの文書を精読し、求められた項目を出力してください。

- 内容概要（summary）: 日本語で要点を簡潔にまとめる。
  - 本願明細書: 発明の課題・解決手段・主要な構成。
  - OA文書: 拒絶理由の種類（新規性/進歩性/記載要件/特許適格性等）、引用文献、対象請求項。
  - 引用文献: 開示内容の要旨（本願との関連が分かる粒度）。
  - 現クレーム（過去の補正書・意見書を含む）: 各請求項・主張の要旨。
- 全文テキスト（full_text）: 文書本文を省略せず原文に忠実に文字起こしする。
  - テキストレイヤの無いスキャンPDFや図中の文字も、内容を読み取って文字起こしする。
  - 表・数式・図の説明も可能な範囲でテキスト化する。要約・改変はしない。

出力は指定された JSON スキーマに厳密に従うこと。`;
